/**
 * @file tetris_lib.h
 * @brief Основной заголовочный файл библиотеки логики игры Tetris
 *
 * Содержит объявления структур данных и функций, необходимых для реализации
 * игровой логики тетриса.
 */
#ifndef TETRIS_LIB_H
#define TETRIS_LIB_H

#include <sqlite3.h>
#include <stdbool.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>

/**
 * @brief Перечисление пользовательских действий
 *
 * Определяет все возможные действия, которые пользователь может совершить
 * в процессе игры.
 */
typedef enum {
  Start,     /**< Начать новую игру */
  Pause,     /**< Поставить игру на паузу/снять с паузы */
  Terminate, /**< Завершить игру */
  Left,      /**< Движение фигуры влево */
  Right,     /**< Движение фигуры вправо */
  Up,        /**< Не используется в данной игре */
  Down,      /**< Движение фигуры вниз. Ускоренное падение фигуры */
  Action     /**< Поворот фигуры */
} UserAction_t;

/**
 * @brief Структура, представляющая игровое поле.
 *
 * Содержит всю информацию, необходимую для отрисовки игрового состояния
 * и управления игровым процессом.
 */
typedef struct {
  int** field;    /**< Двумерный массив 10x20, представляющий игровое поле.
     Каждый элемент соответствует ячейке поля:
     0 - пусто, >0 - заполнено фигурой определенного типа */
  int** next;     /**< Массив, представляющий следующую фигуру для отображения
      в preview */
  int score;      /**< Текущее количество очков */
  int high_score; /**< Рекордное количество очков */
  int level;      /**< Текущий уровень игры */
  int speed;      /**< Скорость падения фигур (в миллисекундах) */
  int pause;      /**< Флаг паузы: 0 - игра активна, 1 - игра на паузе */
} GameInfo_t;

/**
 * @brief Обрабатывает пользовательский ввод
 *
 * Функция принимает действие пользователя и преобразует его в изменения
 * игрового состояния. Вызывается из интерфейса при получении пользовательского
 * ввода.
 *
 * @param[in] action Тип действия пользователя
 * @param[in] hold Флаг зажатия клавиши: true - клавиша зажата, false -
 * одиночное нажатие
 *
 * @note Функция не возвращает значение, но изменяет внутреннее состояние игры
 */
void userInput(UserAction_t action, bool hold);
/**
 * @brief Обновляет и возвращает текущее состояние игры
 *
 * Функция обновляет игровое состояние (падение фигур, проверка линий и т.д.)
 * и возвращает актуальную информацию для отрисовки. Вызывается периодически
 * из игрового цикла.
 *
 * @return GameInfo_t Структура с актуальным состоянием игры
 *
 * @note Функция должна вызываться с определенной периодичностью для
 *       поддержания игрового процесса
 */
GameInfo_t updateCurrentState();

#endif